package org.gatt.domain;
public class InitialTT{

		private int group;
		private int room;
		private int hour;
	
		public int getGroup(){
			return this.group;
		}
		public int getRoom(){
			return this.room;
		}
		public int getHour(){
			return this.hour;
		}
	
		public void setGroup(int group){
			this.group = group;
		}
		public void setRoom(int room){
			this.room = room;
		}
		public void setHour(int hour){
			this.hour = hour;
		}
}